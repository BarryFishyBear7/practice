numberOne = input("Choose a number: ")
print("Your first number is " + str(numberOne))

numberTwo = input("Choose another number: ")
print("Your second number is " + str(numberTwo))

addition = float(numberOne) + float(numberTwo)
print(numberOne + "+" + numberTwo + "= " + str(addition))

subtraction = float(numberOne) - float(numberTwo)
print(numberOne + "-" + numberTwo + "= " + str(subtraction))

multiplication = float(numberOne) * float(numberTwo)
print(numberOne + "*" + numberTwo + "= " + str(multiplication))

division = float(numberOne) / float(numberTwo)
print(numberOne + "/" + numberTwo + "= " + str(division))
